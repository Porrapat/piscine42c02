#include<unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'B')
		{
			i++;
		}
		else if (str[i] >= 'a' && str[i] <= 'b')
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
