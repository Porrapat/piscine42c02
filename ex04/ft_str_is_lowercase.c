int	ft_str_is_lowercase(char *str)
{
	int	is_lowercase;
	int	index;

	is_lowercase = 1;
	index = 0;
	while (str[index] != '\0')
	{
		if (str[index] < 'a' || str[index] > 'z')
		{
			return (0);
		}
		else
		{
			index++;
			continue ;
		}
	}
	return (is_lowercase);
}
