#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(void)
{
	int	a;
	int	b;

	a = 1;
	b = 2;
	ft_putchar(a + '0');
	ft_putchar(b + '0');
	ft_swap(&a, &b);
	ft_putchar(a + '0');
	ft_putchar(b + '0');
	return (0);
}
